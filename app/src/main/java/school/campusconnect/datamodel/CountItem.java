package school.campusconnect.datamodel;

public class CountItem {
    String personalCount, teamCount, commonCount, totalCount;

    public String getPersonalCount() {
        return personalCount;
    }

    public void setPersonalCount(String personalCount) {
        this.personalCount = personalCount;
    }

    public String getTeamCount() {
        return teamCount;
    }

    public void setTeamCount(String teamCount) {
        this.teamCount = teamCount;
    }

    public String getCommonCount() {
        return commonCount;
    }

    public void setCommonCount(String commonCount) {
        this.commonCount = commonCount;
    }

    public String getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(String totalCount) {
        this.totalCount = totalCount;
    }
}
